package com.bdcor.pip.web.sys.version.filter;

import java.util.Date;

import com.bdcor.pip.core.persistence.domain.BaseFilter;

public class PipSysVersionFilter extends BaseFilter{
    private String version;

    private Date versionData;

    private String versionText;
    
    private String startDate;
    
    private String endDate;
    public void setStartDate(String startDate)
	{
		this.startDate = startDate;
	}
    public String getStartDate()
	{
		return startDate;
	}
    public void setEndDate(String endDate)
	{
		this.endDate = endDate;
	}
    public String getEndDate()
	{
		return endDate;
	}
    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public Date getVersionData() {
        return versionData;
    }

    public void setVersionData(Date versionData) {
        this.versionData = versionData;
    }

    public String getVersionText() {
        return versionText;
    }

    public void setVersionText(String versionText) {
        this.versionText = versionText;
    }
}