package com.bdcor.pip.web.spem.filter;

import com.bdcor.pip.core.persistence.domain.BaseFilter;

public class FrozenTubeFilter extends BaseFilter{
	
	private String projectId;
	
	private String tubeCodeLike;
	private String lccIdEq;
	private String fridgeIdEq;
	private String tubeTypeEq;
	private String bloodCodeLike;
	
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getTubeCodeLike() {
		return tubeCodeLike;
	}
	public void setTubeCodeLike(String tubeCodeLike) {
		this.tubeCodeLike = tubeCodeLike;
	}
	public String getLccIdEq() {
		return lccIdEq;
	}
	public void setLccIdEq(String lccIdEq) {
		this.lccIdEq = lccIdEq;
	}
	public String getTubeTypeEq() {
		return tubeTypeEq;
	}
	public void setTubeTypeEq(String tubeTypeEq) {
		this.tubeTypeEq = tubeTypeEq;
	}
	public String getBloodCodeLike() {
		return bloodCodeLike;
	}
	public void setBloodCodeLike(String bloodCodeLike) {
		this.bloodCodeLike = bloodCodeLike;
	}
	public String getFridgeIdEq() {
		return fridgeIdEq;
	}
	public void setFridgeIdEq(String fridgeIdEq) {
		this.fridgeIdEq = fridgeIdEq;
	}
	
}