package com.bdcor.pip.web.spem.filter;

import com.bdcor.pip.core.persistence.domain.BaseFilter;

public class IceBoxRegFilter extends BaseFilter{
    private String bg_date;
    private String end_date;
    private String lcc_code;
    private String project_id;
    
	public String getProject_id() {
		return project_id;
	}
	public void setProject_id(String project_id) {
		this.project_id = project_id;
	}
	public String getBg_date() {
		return bg_date;
	}
	public void setBg_date(String bg_date) {
		this.bg_date = bg_date;
	}
	public String getEnd_date() {
		return end_date;
	}
	public void setEnd_date(String end_date) {
		this.end_date = end_date;
	}
	public String getLcc_code() {
		return lcc_code;
	}
	public void setLcc_code(String lcc_code) {
		this.lcc_code = lcc_code;
	}     
}
