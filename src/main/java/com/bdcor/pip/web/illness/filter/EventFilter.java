package com.bdcor.pip.web.illness.filter;

import com.bdcor.pip.core.persistence.domain.BaseFilter;

public class EventFilter extends BaseFilter  {
	
	private String projectId;
	
	private String lccCode;
	
	private String userId;
	
	private String patientId;
	
	private String patientName;
	
	private int hasEvent;
	
	private String eventName;
	
	private String fileDictCode;
	
	private String fileInfo;
	
	private String startTime;
	
	private String endTime;

	public String getProjectId() {
		return projectId;
	}
	public void setLccCode(String lccCode)
	{
		this.lccCode = lccCode;
	}
	public String getLccCode()
	{
		return lccCode;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getPatientId() {
		return patientId;
	}

	public void setPatientId(String patientId) {
		this.patientId = patientId;
	}

	public int getHasEvent() {
		return hasEvent;
	}

	public void setHasEvent(int hasEvent) {
		this.hasEvent = hasEvent;
	}

	
	public String getFileInfo() {
		return fileInfo;
	}

	public void setFileInfo(String fileInfo) {
		this.fileInfo = fileInfo;
	}

	public String getEventName() {
		return eventName;
	}

	public void setEventName(String eventName) {
		this.eventName = eventName;
	}

	public String getFileDictCode() {
		return fileDictCode;
	}

	public void setFileDictCode(String fileDictCode) {
		this.fileDictCode = fileDictCode;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getPatientName() {
		return patientName;
	}

	public void setPatientName(String patientName) {
		this.patientName = patientName;
	}
	
}
