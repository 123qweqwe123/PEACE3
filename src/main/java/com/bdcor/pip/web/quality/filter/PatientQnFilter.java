package com.bdcor.pip.web.quality.filter;

import com.bdcor.pip.core.persistence.domain.BaseFilter;

public class PatientQnFilter extends BaseFilter {
	
	private String projectId;
	private String userId;

	private String pIdLike;
	private String pIdEq;
	private String patientNameLike;
	private String idNumberLike;
	private String lccCodeEq;
	public String getProjectId() {
		return projectId;
	}
	public void setProjectId(String projectId) {
		this.projectId = projectId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getpIdLike() {
		return pIdLike;
	}
	public void setpIdLike(String pIdLike) {
		this.pIdLike = pIdLike;
	}
	public String getpIdEq() {
		return pIdEq;
	}
	public void setpIdEq(String pIdEq) {
		this.pIdEq = pIdEq;
	}
	public String getPatientNameLike() {
		return patientNameLike;
	}
	public void setPatientNameLike(String patientNameLike) {
		this.patientNameLike = patientNameLike;
	}
	public String getIdNumberLike() {
		return idNumberLike;
	}
	public void setIdNumberLike(String idNumberLike) {
		this.idNumberLike = idNumberLike;
	}
	public String getLccCodeEq() {
		return lccCodeEq;
	}
	public void setLccCodeEq(String lccCodeEq) {
		this.lccCodeEq = lccCodeEq;
	}
	
}
