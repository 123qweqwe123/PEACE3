package com.bdcor.pip.data.xml.vo;

import javax.xml.bind.annotation.XmlAttribute;

public class Attribute {
	
	
	private String id;
	
	
	private String type;
	
	
	private String validatetype;

	
	private String value;
	
	private String validateobjecttype;
	
	@XmlAttribute(name = "validateobjecttype")
   	public String getValidateobjecttype() {
		return validateobjecttype;
	}

	public void setValidateobjecttype(String validateobjecttype) {
		this.validateobjecttype = validateobjecttype;
	}

	@XmlAttribute(name = "id")
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	@XmlAttribute(name = "type")
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	@XmlAttribute(name = "validatetype")
	public String getValidatetype() {
		return validatetype;
	}

	public void setValidatetype(String validatetype) {
		this.validatetype = validatetype;
	}

	@XmlAttribute(name = "value")
	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
}
