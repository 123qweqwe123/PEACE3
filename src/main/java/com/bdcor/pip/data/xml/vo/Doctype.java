package com.bdcor.pip.data.xml.vo;

import javax.xml.bind.annotation.XmlAttribute;

public class Doctype {
	
	
	private String projectid;
	
	
	private String projectname;

	
	private String uqsId;
	
	
	private String usqName;
	
	
	private String uqsVersion;
	
	
	private String versionCreateDate;

	@XmlAttribute(name = "projectid")
	public String getProjectid() {
		return projectid;
	}

	public void setProjectid(String projectid) {
		this.projectid = projectid;
	}

	@XmlAttribute(name = "projectname")
	public String getProjectname() {
		return projectname;
	}

	public void setProjectname(String projectname) {
		this.projectname = projectname;
	}

	@XmlAttribute(name = "UQSid")
	public String getUqsId() {
		return uqsId;
	}

	public void setUqsId(String uqsId) {
		this.uqsId = uqsId;
	}

	@XmlAttribute(name = "UQSName")
	public String getUsqName() {
		return usqName;
	}

	public void setUsqName(String usqName) {
		this.usqName = usqName;
	}

	@XmlAttribute(name = "UQSVersion")
	public String getUqsVersion() {
		return uqsVersion;
	}

	public void setUqsVersion(String uqsVersion) {
		this.uqsVersion = uqsVersion;
	}

	@XmlAttribute(name = "VersionCreateDate")
	public String getVersionCreateDate() {
		return versionCreateDate;
	}

	public void setVersionCreateDate(String versionCreateDate) {
		this.versionCreateDate = versionCreateDate;
	}
	
	
}
